    <h2>Gestion des frais des visiteurs</h2>
    <p>Bienvenue dans votre application de gestion des frais de deplacements. </p>
    <p>Afin de couvrir les frais que vous avez engages lors de vos deplacements professionnels, vous devez completer des fiches de frais mensuelles.</p>
        <ul>
            <li>Une fiche de frais court du 1er au dernier jour du mois</li>
            <li>Les fiches de frais sont creees automatiquement par l'application au fil de votre utilisation</li>
            <li>Vous completez les fiches de frais a votre rythme</li>
            <li>Lorsqu'une fiche est totalement renseignee, vous devrez la "signer" afin qu'elle puisse etre traitee par le service comptable</li>
        </ul>
 
        <p>Au moyen du bandeau gauche, vous avez acces aux fonctionalites du profil visiteur : </p>
        <ul>
            <li>Gerer vos fiches de frais (modifier, signer, imprimer)</li>
            <li>Se deconnecter</li>
        </ul>
   
</div>